% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

%Assign all values
out_files = cellstr(get(handles.listbox2,'String'));
destination = get(handles.edit2, 'String');
mincontact = str2double(get(handles.edit3, 'String'));
maxcontact = str2double(get(handles.edit4, 'String'));
minnocontact = str2double(get(handles.edit5, 'String'));
maxnocontact = str2double(get(handles.edit7, 'String'));
boutdeterminant = str2double(get(handles.edit9, 'String'));
ms = str2double(get(handles.edit10, 'String'));
noise = str2double(get(handles.edit8, 'String'));
contents = cellstr(get(handles.popupmenu2,'String'));
plotMS = contents{get(handles.popupmenu2,'Value')};
for i = 1:length(out_files)
    file = out_files{1};
    if length(file)<5 || ~strcmp(file(end-3:end),'.txt')
        msgbox('Select a text file to process!');
        break
    elseif str2double(destination) == 0
        msgbox('Select a destination to save data!');
        break
    else
        data_process_mag_v9(out_files, destination, mincontact, maxcontact, minnocontact, maxnocontact, boutdeterminant, ms, noise, plotMS);
        break
    end
end


